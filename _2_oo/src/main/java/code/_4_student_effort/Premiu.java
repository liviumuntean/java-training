package code._4_student_effort;

public class Premiu {
    private String name;
    private Integer year;

    public Premiu(){}

    public Premiu(String name, Integer year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public Integer getYear() {
        return year;
    }

}
